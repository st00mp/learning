<h1>Recette 2 : Salade César</h1>
<h2>Ingrédients :</h2>
<ul>
    <li>1 laitue</li>
    <li>150g de poulet grillé</li>
    <li>50g de parmesan</li>
    <li>Croûtons</li>
    <li>Sauce César</li>
</ul>
<h2>Étapes :</h2>
<p>1. Lavez la laitue et coupez-la en morceaux.</p>
<p>2. Ajoutez le poulet, le parmesan et les croûtons.</p>
<p>3. Mélangez avec la sauce César avant de servir.</p>