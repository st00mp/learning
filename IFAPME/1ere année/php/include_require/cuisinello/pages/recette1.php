<h1>Recette 1 : Gâteau au chocolat</h1>
<h2>Ingrédients :</h2>
<ul>
    <li>200g de chocolat</li>
    <li>100g de beurre</li>
    <li>150g de sucre</li>
    <li>4 œufs</li>
    <li>80g de farine</li>
</ul>
<h2>Étapes :</h2>
<p>1. Faites fondre le chocolat avec le beurre.</p>
<p>2. Mélangez le sucre et les œufs.</p>
<p>3. Ajoutez le chocolat fondu et la farine.</p>
<p>4. Versez dans un moule et faites cuire 25 minutes à 180°C.</p>